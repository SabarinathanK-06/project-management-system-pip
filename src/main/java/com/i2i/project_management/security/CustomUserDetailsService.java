package com.i2i.project_management.security;

import com.i2i.project_management.repository.UserRepository;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;

@Service
@RequiredArgsConstructor
@Slf4j
public class CustomUserDetailsService implements UserDetailsService {

    private final UserRepository userRepository;

    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        log.debug("Loading user details for {}", username);
        return userRepository.findByEmailWithRoles(username)
                .map(CustomUserDetails::new)
                .orElseThrow(() -> {
                    log.warn("User {} not found during authentication", username);
                    return new UsernameNotFoundException("User not found");
                });
    }
}

